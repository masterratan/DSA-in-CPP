#include<iostream>
using namespace std;
int main(){
    cout<<sizeof(int)<<endl; // int -> 4 bytes
    cout<<sizeof(float)<<endl; // float -> 4 bytes
    cout<<sizeof(char)<<endl; // char -> 1 bytes
    cout<<sizeof(bool)<<endl; // bool -> 1 bytes
    cout<<sizeof(long long)<<endl; // long long -> 8 bytes
    cout<<sizeof(double)<<endl; // double -> 8 bytes
}